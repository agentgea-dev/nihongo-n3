// Fungsi helper
function getGrammarByStage(stageNum) {
  return GRAMMAR_DB.filter(g => g.stage === stageNum);
}

function buildGrammarFlashcards(grammarList) {
  return grammarList.map(g => ({
    jp: g.jp,
    read: "",
    meaning: g.meaning,
    example: g.example,
    exampleId: g.exampleId,
    mnemonic: g.mnemonic,
  }));
}

function buildGrammarQuestions(grammarList) {
  return grammarList.map(g => ({
    jp: g.quizSentence,
    furi: g.quizFuri || "",
    prompt: g.quizPrompt,
    opts: g.quizOpts,
    ans: g.quizAns,
    exp: g.quizExp,
  }));
}

